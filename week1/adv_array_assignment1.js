let cart = [
  { id: 101, name: "Laptop", price: 60000, quantity: 1, inStock: true },
  { id: 102, name: "Mouse", price: 800, quantity: 2, inStock: true },
  { id: 103, name: "Keyboard", price: 1500, quantity: 1, inStock: false },
  { id: 104, name: "Monitor", price: 12000, quantity: 1, inStock: true }
];

// print only in-stock items
let result=cart.filter(element=> element.inStock===true)
console.log("InStock Items=",result)

// create a new array with {name,totalprice}
let result2=cart.map(element=>({name:element.name,
    totalprice:element.price*element.quantity
}))
console.log(result2)

//calculate grand total of the cart
let result3=cart.reduce((acc,element)=>acc+(element.price*element.quantity),0)
console.log("Grand Total=",result3)

//get the details of mouse
let result4=cart.find(element=> element.name==="Mouse")
console.log("Mouse Details=",result4)

//find the index of keyboard
let result5=cart.findIndex(element=> element.name==="Keyboard")
console.log("Keyboard Index=",result5)